#include <iostream> // Includes input/output stream library

int main() {
  std::cout << "Hello World!" << std::endl; // Print to console
  return 0; 
}


// #include <iostream> pulls in functionality to output text to screen (std::cout)
// main() - the entry point of every c++ program 
// std::cout - outputs text; the std:: means it's part of the standard library 
// return 0; - indicates the program ran successfully

// With game dev std::cout is used early on for debugging game objects, printing player positions and displaying simple text UIs before using graphics