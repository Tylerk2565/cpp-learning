#include <iostream> // Includes input/output stream library

int main() {
  std::cout << "Hello World!" << std::endl; // Print to console
  return 0; 
}


// #include <iostream> tells the compiler to include the input/output library
// int main() - every c++ program starts here
// std::cout - stands for "character output" this sends data to the console (like console.log)
// << - the "insertion" operator
// std::endl - new line
// return 0 - exits program on success
// later std::cout helps you debug - printing player health, enemy count, collision flags, etc.

