#include <iostream>

int main()
{
  int score = 150;
  float health = 87.5f;
  char grade = 'A';
  bool isAlive = true;

  std::cout << "Score: " << score << std::endl;
  std::cout << "Health: " << health << std::endl;
  std::cout << "Grade: " << grade << std::endl;
  std::cout << "Alive? " << isAlive << std::endl;
}

// int - whole numbers
// float - decimal numbers
// char - a single character
// bool - true or false
